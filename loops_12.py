# String : string is an unicode sequence collection of characters which store the char in ordered manner , it store the items inside single double and multiple quotes, it is immutable that means not change after assignment...

s1 = "Universal"
# access elements from the index  positive index 
print(s1[0])
# access elements from the negative indexing 
print(s1[-1])
# print 2 to 7 elements
# s1[start : end ]
print(s1[2:7])
# s1[start : end : step ]
print(s1[8:2:-1])
print(s1[-1:-9:-1])
